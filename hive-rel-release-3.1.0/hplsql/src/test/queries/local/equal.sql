PRINT 'Case 1 = 1';
IF 1 = 1 THEN
  PRINT 'Equal - Correct';
ELSE
  PRINT 'Not equal - Incorrect';
END IF;

PRINT 'Case 1 == 1';
IF 1 == 1 THEN
  PRINT 'Equal - Correct';
ELSE
  PRINT 'Not equal - Incorrect';
END IF;

PRINT 'Case 1 <> 3';
IF 1 <> 3 THEN
  PRINT 'Not equal - Correct';
ELSE
  PRINT 'Equal - Incorrect';
END IF;

PRINT 'Case 1 != 3';
IF 1 != 3 THEN
  PRINT 'Not equal - Correct';
ELSE
  PRINT 'Equal - Incorrect';
END IF;

PRINT 'Case 3 > 1';
IF 3 > 1 THEN
  PRINT 'Greater - Correct';
ELSE
  PRINT 'Greater - Incorrect';
END IF;

PRINT 'Case 1 < 3';
IF 1 < 3 THEN
  PRINT 'Less - Correct';
ELSE
  PRINT 'Less - Incorrect';
END IF;

PRINT 'Case 3 >= 1';
IF 3 >= 1 THEN
  PRINT 'Greater or equal - Correct';
ELSE
  PRINT 'Greater or equal - Incorrect';
END IF;

PRINT 'Case 1 <= 3';
IF 1 <= 3 THEN
  PRINT 'Less or equal - Correct';
ELSE
  PRINT 'Less or equal - Incorrect';
END IF;